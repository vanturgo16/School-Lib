     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required'
        ]);

        $store=BookCategory::create([
            'category_name' => $request->name
        ]);

        if($store){
            return redirect('/masters/categories')->with('status','Success Create Category');
        }
        else{
            return redirect('/masters/categories')->with('status','Failed Create Category');
        }
    }

